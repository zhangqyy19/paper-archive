// One-off generator: downloads world-atlas land-110m TopoJSON and writes it
// inlined into src/components/map/worldTopology.ts as a typed const.
// Run: node scripts/gen-world-topology.mjs
// This keeps the map's coastline data fully offline / zero runtime deps while
// guaranteeing the arc data is complete and correctly ordered.
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const SRC = 'https://cdn.jsdelivr.net/npm/world-atlas@2/land-110m.json'
const __dirname = dirname(fileURLToPath(import.meta.url))
const OUT = join(__dirname, '..', 'src', 'components', 'map', 'worldTopology.ts')

const res = await fetch(SRC)
if (!res.ok) throw new Error(`Failed to fetch land-110m.json: ${res.status}`)
const topo = await res.json()

const header =
  '// Inlined world-atlas land-110m TopoJSON (Natural Earth land coastlines).\n' +
  '// Delta-encoded + quantized; decoded in `worldLand.ts`. Zero runtime deps.\n' +
  '// Generated by scripts/gen-world-topology.mjs — do not edit by hand.\n' +
  '/* eslint-disable */\n'

const body = `export const LAND_TOPOLOGY = ${JSON.stringify(topo)} as const\n`

writeFileSync(OUT, header + body, 'utf8')
console.log(`Wrote ${OUT} (${(header + body).length} bytes)`)